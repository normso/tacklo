package utils

func IsGameCompleted(b *[3][3]int8) bool {
	if ((b[0][0] != 0 && b[0][1] != 0 && b[0][2] != 0) && (b[0][0] == b[0][1]) && (b[0][0] == b[0][2])) ||
		((b[1][0] != 0 && b[1][1] != 0 && b[1][2] != 0) && (b[1][0] == b[1][1]) && (b[1][0] == b[1][2])) ||
		((b[2][0] != 0 && b[2][1] != 0 && b[2][2] != 0) && (b[2][0] == b[2][1]) && (b[2][0] == b[2][2])) ||
		((b[0][0] != 0 && b[1][0] != 0 && b[2][0] != 0) && (b[0][0] == b[1][0]) && (b[0][0] == b[2][0])) ||
		((b[0][1] != 0 && b[1][1] != 0 && b[2][1] != 0) && (b[0][1] == b[1][1]) && (b[0][1] == b[2][1])) ||
		((b[0][2] != 0 && b[1][2] != 0 && b[2][2] != 0) && (b[0][2] == b[1][2]) && (b[0][2] == b[2][2])) ||
		((b[0][0] != 0 && b[1][1] != 0 && b[2][2] != 0) && (b[0][0] == b[1][1]) && (b[0][0] == b[2][2])) ||
		((b[0][2] != 0 && b[1][1] != 0 && b[2][0] != 0) && (b[0][2] == b[1][1]) && (b[0][2] == b[2][0])) {
		return true
	}
	return false
}
